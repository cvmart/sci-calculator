namespace Trigonometric
{
    public class Arithmetic
    {
        public static double sine(double a)
        {
            return Math.Sin(a);
        }

        public static double cosine(double a)
        {
            return Math.Cos(a);
        }

        public static double tangent(double a)
        {
            return Math.Tan(a);
        }
    }
}
